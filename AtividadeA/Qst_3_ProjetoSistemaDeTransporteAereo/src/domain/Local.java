package domain;

public class Local {

	private String cidade;
	private String estado;
	private String pais;
	private String endereco;
	
	public Local(String cidade, String estado, String pais, String endereco) {
		
		this.cidade = cidade;
		this.estado = estado;
		this.pais = pais;
		this.endereco = endereco;
	}
	
}
